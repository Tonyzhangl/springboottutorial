package com.kirago.chapter20.pojo;

public class Role {

    private Long id;

    private String name;

    public void setId(Long id){
        this.id = id;
    }

    public void setName(String name){
        this.name = name;
    }

    public Long getId(){
        return id;
    }

    public String getName(){
        return name;
    }

}
